					<h3>Our Beliefs</h3>
						<ul>
							<li>We believe in one <strong>God</strong>, who created the world and all that is in it. We affirm that God is known and experienced as the Triune God: <strong>Father, Son, and Holy Spirit</strong>.</li>
							<li>We believe that <strong>Jesus</strong> is the incarnation, the very embodiment of God’s loving presence and power. We maintain that Christ is both fully human and fully divine. We affirm Christ’s saving work on the cross and that through his life, death and resurrection “God was reconciling the world to himself, not counting their trespasses against them, and entrusting the message of reconciliation to us.” (2 Corinthians 5:19)</li>
							<li>We believe the <strong>Holy Spirit</strong> works within personal experience and in the life of the Christian community to make God’s redemptive love a tangible reality in our lives and in the world.</li>
							<li>We believe the <strong>Bible</strong> is God’s written Word that reveals the character of God. We believe that it contains the truth about God, about life, and about the relationship between God and all of creation.
</li>
							<li>We believe that <strong>Grace</strong> is the undeserved, unearned, unrepayable gift of the God who loves us enough to meet us where we are, but loves us too much to leave us there. Grace is the love of God at work within us to transform each of our lives into a unique expression of the love of God revealed in Jesus Christ, so that we become participants in God’s transformation of the world.</li>
							<li>We believe that the <strong>Church</strong> is called to make disciples of Jesus Christ for the transformation of the world. It is the Body of Christ which worships, serves, and lives the life of faith together. The Church is the community that is called to live now as if God’s future has already come.</li>
							<li>We believe that the <strong>Kingdom of God</strong> is both a present reality and a future hope. God’s kingdom is breaking in upon the world bringing peace and justice, compassion and hope, and the fulfillment of all God’s purposes. Wherever God’s power is, there too is God’s Kingdom.</li>
						</ul>
						<p><strong>You are welcome here.</strong> You do not have to believe all of the above statements to be part of this church community. Many of us wrestle with these beliefs while participating in making God’s love real in our community and the world. 
</p>
